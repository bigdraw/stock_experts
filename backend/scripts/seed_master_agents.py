async def seed_master_agents():
    """Idempotent: insert 8 default master agents if absent. Safe to call on every startup."""
    created = 0
    skipped = 0
    async with async_session_factory() as db:
        for name, spec in MASTERS.items():
            existing = await db.execute(
                select(Agent).where(Agent.name == name, Agent.type == "master")
            )
            if existing.scalar_one_or_none() is not None:
                skipped += 1
                continue
            agent = Agent(
                name=name,
                type="master",
                description=spec["description"],
                system_prompt=spec["philosophy"] + TOOL_FOOTER,
                config=json.dumps(spec["config"]),
            )
            db.add(agent)
            created += 1
            log.info(f"  created master agent: {name}")
        await db.commit()
    log.info(f"Master agents seed: {created} created, {skipped} skipped (already exist).")


async def main():
    await seed_master_agents()


if __name__ == "__main__":
    asyncio.run(main())
